package me.hazedev.shooter;

public enum UpgradeType {

    HEALTH, // Increase max health
    FIRE_RATE, // Increase fire rate
    DAMAGE, // Increase bullet damage
    SPEED, // Increase movement speed
    PENETRATION, // Increase amount of enemies bullet passes through
    REGENERATION, // Instantly Regenerate health
    RECHARGE_RATE, // Gun recharge rate
    HEAT_CAPACITY; // How much you can fire before overheating

}
